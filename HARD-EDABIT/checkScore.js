// Add the Values of the Symbols in a Matrix

function checkScore(str) {
  const obj = {
    "#": 5,
    O: 3,
    X: 1,
    "!": -1,
    "!!": -3,
    "!!!": -5,
  }
  let arr = str.flat(Infinity)
  //   console.log(arr)
  let sum = arr.reduce((acc, curr) => acc + obj[curr], 0)
  //   console.log(sum)
  let max = Math.max(sum, 0)
  return max
}

